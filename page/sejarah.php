        <div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Dengan berakhirnya masa kesultanan Jambi menyusul gugurnya Sulthan Thaha Saifuddin tanggal 27 April 1904 dan berhasilnya Belanda menguasai wilayah-wilayah Kesultanan Jambi, maka Jambi ditetapkan sebagai Keresidenan dan masuk ke dalam wilayah Nederlandsch Indie. Residen Jambi yang pertama O.L Helfrich yang diangkat berdasarkan Keputusan Gubernur Jenderal Belanda No. 20 tanggal 4 Mei 1906 dan pelantikannya dilaksanakan tanggal 2 Juli 1906.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Kekuasan Belanda atas Jambi berlangsung &plusmn; 36 tahun karena pada tanggal 9 Maret 1942 terjadi peralihan kekuasaan kepada Pemerintahan Jepang. Dan pada 14 Agustus 1945 Jepang menyerah pada sekutu. Tanggal 17 Agustus 1945 diproklamirkanlah Negara Republik Indonesia. Sumatera disaat Proklamasi tersebut menjadi satu Provinsi yaitu Provinsi Sumatera dan Medan sebagai ibukotanya dan MR. Teuku Muhammad Hasan ditunjuk memegangkan jabatan Gubernurnya.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Pada tanggal 18 April 1946 Komite Nasional Indonesia Sumatera bersidang di Bukittinggi memutuskan Provinsi Sumatera terdiri dari tiga Sub Provinsi yaitu Sub Provinsi Sumatera Utara, Sumatera Tengah dan Sumatera Selatan.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Sub Provinsi Sumatera Tengah mencakup keresidenan Sumatra Barat, Riau dan Jambi. Tarik menarik Keresidenan Jambi untuk masuk ke Sumatera Selatan atau Sumatera Tengah ternyata cukup alot dan akhirnya ditetapkan dengan pemungutan suara pada Sidang KNI Sumatera tersebut dan Keresidenan Jambi masuk ke Sumatera Tengah. Sub-sub Provinsi dari Provinsi Sumatera ini kemudian dengan undang-undang nomor 10 tahun 1948 ditetapkan sebagai Provinsi.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Dengan UU.No. 22 tahun 1948 tentang Pokok-pokok Pemerintahan Daerah keresidenan Jambi saat itu terdiri dari 2 Kabupaten dan 1 Kota Praja Jambi. Kabupaten-kabupaten tersebut adalah Kabupaten Merangin yang mencakup Kewedanaan Muara Tebo, Muaro Bungo, Bangko dan Batanghari terdiri dari kewedanaan Muara Tembesi, Jambi Luar Kota, dan Kuala Tungkal. Masa terus berjalan, banyak pemuka masyarakat yang ingin keresidenan Jambi untuk menjadi bagian Sumatera Selatan dan dibagian lain ingin tetap bahkan ada yang ingin berdiri sendiri. Terlebih dari itu, Kerinci kembali dikehendaki masuk Keresidenan Jambi, karena sejak tanggal 1 Juni 1922 Kerinci yang tadinya bagian dari Kesultanan Jambi dimasukkan ke keresidenan Sumatera Barat tepatnya jadi bagian dari Kabupaten Pesisir Selatan dan Kerinci (PSK)
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Tuntutan keresidenan Jambi menjadi daerah Tingkat I Provinsi diangkat dalam Pernyataan Bersama antara Himpunan Pemuda Merangin Batanghari (HP.MERBAHARI) dengan Front Pemuda Jambi (FROPEJA) Tanggal 10 April 1954 yang diserahkan langsung Kepada Bung Hatta Wakil Presiden di Bangko, yang ketika itu berkunjung kesana. Penduduk Jambi saat itu tercatat kurang lebih 500.000 jiwa (tidak termasuk Kerinci)
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Keinginan tersebut diwujudkan kembali dalam Kongres Pemuda se-Daerah Jambi 30 April &ndash; 3 Mei 1954 dengan mengutus tiga orang delegasi yaitu Rd. Abdullah, AT Hanafiah dan H. Said serta seorang penasehat delegasi yaitu Bapak Syamsu Bahrun menghadap Mendagri Prof. DR.MR Hazairin.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Berbagai kebulatan tekad setelah itu bermunculan baik oleh gabungan parpol, Dewan Pemerintahan Marga, Dewan Perwakilan Rakyat Daerah Merangin, Batanghari. Puncaknya pada kongres rakyat Jambi 14-18 Juni 1955 di gedung bioskop Murni terbentuklah wadah perjuangan Rakyat Jambi bernama Badan Kongres Rakyat Djambi (BKRD) untuk mengupayakan dan memperjuangkan Jambi menjadi Daerah Otonomi Tingkat I Provinsi Jambi.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Pada Kongres Pemuda se-daerah Jambi tanggal 2-5 Januari 1957 mendesak BKRD menyatakan Keresidenan Jambi secara de facto menjadi Provinsi selambat-lambatnya tanggal 9 Januari 1957 . Sidang Pleno BKRD tanggal 6 Januari 1957 pukul 02.00 dengan resmi menetapkan keresidenan Jambi menjadi Daerah Otonomi Tingkat I Provinsi yang berhubungan langsung dengan pemerintah pusat dan keluar dari Provinsi Sumatera Tengah. Dewan Banteng selaku penguasa pemerintah Provinsi Sumatera Tengah yang telah mengambil alih pemerintahan Provinsi Sumatera Tengah dari Gubernur Ruslan Mulyohardjo pada tanggal 9 Januari 1957 menyetujui keputusan BKRD.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Pada tanggal 8 Februari 1957 Ketua Dewan Banteng Letkol Ahmad Husein melantik Residen Djamin gr. Datuk Bagindo sebagai acting Gubernur dan H. Hanafi sebagai wakil Acting Gubernur Provinsi Djambi, dengan staff 11 orang yaitu Nuhan, Rd. Hasan Amin, M. Adnan Kasim, H.A. Manap, Salim, Syamsu Bahrun, Kms. H.A.Somad. Rd. Suhur, Manan, Imron Nungcik dan Abd Umar yang dikukuhkan dengan SK No. 009/KD/U/L KPTS. tertanggal 8 Februari 1957 dan sekaligus meresmikan berdirinya Provinsi Jambi di halaman rumah Residen Jambi (kini Gubernuran Jambi).
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Pada tanggal 9 Agustus 1957 Presiden RI Ir. Soekarno akhirnya menandatangani di Denpasar Bali. UU Darurat No. 19 tahun 1957 tentang Pembentukan Provinsi Sumatera Barat, Riau dan Jambi. Dengan UU No. 61 tahun 1958 tanggal 25 Juli 1958 UU Darurat No. 19 Tahun 1957 Tentang Pembentukan Daerah Sumatera Tingkat I Sumatera Barat, Djambi dan Riau. (UU tahun 1957 No. 75) sebagai Undang-undang.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Dalam UU No. 61 tahun 1958 disebutkan pada pasal 1 hurup b, bahwa daerah Swatantra Tingkat I Jambi wilayahnya mencakup wilayah daerah Swatantra Tingkat II Batanghari, Merangin, dan Kota Praja Jambi serta Kecamatan-Kecamatan Kerinci Hulu, Tengah dan Hilir.
</font>
</div>
<div style="text-align: justify">
<font size="3"><br />
</font>
</div>
<div style="text-align: justify">
<font size="3"><span style="white-space: pre">	</span>Kelanjutan UU No. 61 tahun 1958 tersebut pada tanggal 19 Desember 1958 Mendagri Sanoesi Hardjadinata mengangkat dan menetapkan Djamin gr. Datuk Bagindo Residen Jambi sebagai Dienst Doend DD Gubernur (residen yang ditugaskan sebagai Gubernur Provinsi Jambi dengan SK Nomor UP/5/8/4). Pejabat Gubernur pada tanggal 30 Desember 1958 meresmikan berdirinya Provinsi Jambi atas nama Mendagri di Gedung Nasional Jambi (sekarang gedung BKOW). Kendati dejure Provinsi Jambi di tetapkan dengan UU Darurat 1957 dan kemudian UU No. 61 tahun 1958 tetapi dengan pertimbangan sejarah asal-usul pembentukannya oleh masyarakat Jambi melalui BKRD maka tanggal Keputusan BKRD 6 Januari 1957 ditetapkan sebagai hari jadi Provinsi Jambi, sebagaimana tertuang dalam Peraturan Daerah Provinsi Djambi Nomor. 1 Tahun 1970 tanggal 7 Juni 1970 tentang Hari Lahir Provinsi Djambi.
</font>
</div>
